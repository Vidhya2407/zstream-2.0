export function getRandomImage(category: keyof typeof contentImages): UnsplashImage {
  const images = contentImages[category];
  return images[Math.floor(Math.random() * images.length)];
}

export function getImageAttribution(image: UnsplashImage): string {
  return `Photo by ${image.photographer} on Unsplash`;
}

